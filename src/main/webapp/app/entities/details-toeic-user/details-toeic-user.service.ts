import axios from 'axios';

import buildPaginationQueryOpts from '@/shared/sort/sorts';

import { IDetailsToeicUser } from '@/shared/model/details-toeic-user.model';

const baseApiUrl = 'api/details-toeic-users';

export default class DetailsToeicUserService {
  public find(id: number): Promise<IDetailsToeicUser> {
    return new Promise<IDetailsToeicUser>((resolve, reject) => {
      axios
        .get(`${baseApiUrl}/${id}`)
        .then(res => {
          resolve(res.data);
        })
        .catch(err => {
          reject(err);
        });
    });
  }

  public retrieve(paginationQuery?: any): Promise<any> {
    return new Promise<any>((resolve, reject) => {
      axios
        .get(baseApiUrl + `?${buildPaginationQueryOpts(paginationQuery)}`)
        .then(res => {
          resolve(res);
        })
        .catch(err => {
          reject(err);
        });
    });
  }

  public delete(id: number): Promise<any> {
    return new Promise<any>((resolve, reject) => {
      axios
        .delete(`${baseApiUrl}/${id}`)
        .then(res => {
          resolve(res);
        })
        .catch(err => {
          reject(err);
        });
    });
  }

  public create(entity: IDetailsToeicUser): Promise<IDetailsToeicUser> {
    return new Promise<IDetailsToeicUser>((resolve, reject) => {
      axios
        .post(`${baseApiUrl}`, entity)
        .then(res => {
          resolve(res.data);
        })
        .catch(err => {
          reject(err);
        });
    });
  }

  public update(entity: IDetailsToeicUser): Promise<IDetailsToeicUser> {
    return new Promise<IDetailsToeicUser>((resolve, reject) => {
      axios
        .put(`${baseApiUrl}/${entity.id}`, entity)
        .then(res => {
          resolve(res.data);
        })
        .catch(err => {
          reject(err);
        });
    });
  }

  public partialUpdate(entity: IDetailsToeicUser): Promise<IDetailsToeicUser> {
    return new Promise<IDetailsToeicUser>((resolve, reject) => {
      axios
        .patch(`${baseApiUrl}/${entity.id}`, entity)
        .then(res => {
          resolve(res.data);
        })
        .catch(err => {
          reject(err);
        });
    });
  }
}
