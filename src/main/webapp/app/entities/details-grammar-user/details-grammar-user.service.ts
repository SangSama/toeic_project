import axios from 'axios';

import buildPaginationQueryOpts from '@/shared/sort/sorts';

import { IDetailsGrammarUser } from '@/shared/model/details-grammar-user.model';

const baseApiUrl = 'api/details-grammar-users';

export default class DetailsGrammarUserService {
  public find(id: number): Promise<IDetailsGrammarUser> {
    return new Promise<IDetailsGrammarUser>((resolve, reject) => {
      axios
        .get(`${baseApiUrl}/${id}`)
        .then(res => {
          resolve(res.data);
        })
        .catch(err => {
          reject(err);
        });
    });
  }

  public retrieve(paginationQuery?: any): Promise<any> {
    return new Promise<any>((resolve, reject) => {
      axios
        .get(baseApiUrl + `?${buildPaginationQueryOpts(paginationQuery)}`)
        .then(res => {
          resolve(res);
        })
        .catch(err => {
          reject(err);
        });
    });
  }

  public delete(id: number): Promise<any> {
    return new Promise<any>((resolve, reject) => {
      axios
        .delete(`${baseApiUrl}/${id}`)
        .then(res => {
          resolve(res);
        })
        .catch(err => {
          reject(err);
        });
    });
  }

  public create(entity: IDetailsGrammarUser): Promise<IDetailsGrammarUser> {
    return new Promise<IDetailsGrammarUser>((resolve, reject) => {
      axios
        .post(`${baseApiUrl}`, entity)
        .then(res => {
          resolve(res.data);
        })
        .catch(err => {
          reject(err);
        });
    });
  }

  public update(entity: IDetailsGrammarUser): Promise<IDetailsGrammarUser> {
    return new Promise<IDetailsGrammarUser>((resolve, reject) => {
      axios
        .put(`${baseApiUrl}/${entity.id}`, entity)
        .then(res => {
          resolve(res.data);
        })
        .catch(err => {
          reject(err);
        });
    });
  }

  public partialUpdate(entity: IDetailsGrammarUser): Promise<IDetailsGrammarUser> {
    return new Promise<IDetailsGrammarUser>((resolve, reject) => {
      axios
        .patch(`${baseApiUrl}/${entity.id}`, entity)
        .then(res => {
          resolve(res.data);
        })
        .catch(err => {
          reject(err);
        });
    });
  }
}
